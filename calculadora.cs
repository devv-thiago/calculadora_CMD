public class Calculadora
{
    public int numero1;
    public int numero2;

    public adicao(int num1, int num2)
    {
        return num1 + num2;
    }
    public divisao(int num1, int num2)
    {
        return num1 / num2;
    }
    public multiplicacao(int num1, int num2)
    {
        return num1 * num2;
    }
    public subtracao(int num1, int num2)
    {
        return num1 - num2;
    }

    
}